#!/usr/bin/env python3
"""Generate large SQL test files for benchmarking sqruff vs sqlfluff.

Usage:
    python generate_large_sql.py [--lines 10000] [--output large_test.sql]
"""

import argparse
import random
import string

TABLES = ["users", "orders", "products", "inventory", "audit_log", "sessions",
          "employees", "departments", "categories", "reviews"]

COLUMNS = {
    "users": ["id", "name", "email", "active", "created_at", "role"],
    "orders": ["id", "user_id", "total", "status", "order_date"],
    "products": ["id", "name", "price", "category_id", "stock"],
    "inventory": ["id", "product_id", "warehouse_id", "quantity"],
    "audit_log": ["id", "user_id", "action", "details", "created_at"],
    "sessions": ["id", "user_id", "token", "expired_at"],
    "employees": ["id", "name", "department_id", "salary", "hire_date"],
    "departments": ["id", "name", "budget"],
    "categories": ["id", "name", "parent_id"],
    "reviews": ["id", "product_id", "user_id", "rating", "comment"],
}


def random_string(length=8):
    return ''.join(random.choices(string.ascii_lowercase, k=length))


def gen_select(i):
    table = random.choice(TABLES)
    cols = COLUMNS[table]
    num_cols = random.randint(1, len(cols))
    selected = random.sample(cols, num_cols)

    sql = f"SELECT {', '.join(selected)} FROM {table}"

    # Sometimes add JOINs
    if random.random() > 0.5:
        join_table = random.choice([t for t in TABLES if t != table])
        alias1 = table[0]
        alias2 = join_table[0] + "2" if join_table[0] == table[0] else join_table[0]
        sql = f"SELECT {alias1}.id, {alias2}.id FROM {table} {alias1} INNER JOIN {join_table} {alias2} ON {alias1}.id = {alias2}.{table[:-1]}_id"

    # WHERE clause
    if random.random() > 0.3:
        sql += f" WHERE {cols[0]} > {i}"

    # ORDER BY
    if random.random() > 0.6:
        sql += f" ORDER BY {random.choice(cols)} {'DESC' if random.random() > 0.5 else 'ASC'}"

    # LIMIT
    if random.random() > 0.5:
        sql += f" LIMIT {random.randint(10, 1000)}"

    return sql + ";"


def gen_insert(i):
    table = random.choice(TABLES)
    cols = COLUMNS[table][1:]  # skip id
    num_rows = random.randint(1, 3)
    rows = []
    for _ in range(num_rows):
        vals = []
        for c in cols:
            if "id" in c or c in ("rating", "quantity", "stock"):
                vals.append(str(random.randint(1, 10000)))
            elif c in ("price", "total", "salary", "budget"):
                vals.append(f"{random.uniform(1, 10000):.2f}")
            elif c in ("active",):
                vals.append(str(random.randint(0, 1)))
            else:
                vals.append(f"'{random_string()}'")
        rows.append(f"({', '.join(vals)})")

    return f"INSERT INTO {table} ({', '.join(cols)}) VALUES {', '.join(rows)};"


def gen_update(i):
    table = random.choice(TABLES)
    cols = COLUMNS[table]
    set_col = random.choice(cols[1:])
    if "id" in set_col or set_col in ("rating", "quantity", "stock", "active"):
        val = str(random.randint(0, 100))
    elif set_col in ("price", "total", "salary"):
        val = f"{random.uniform(1, 10000):.2f}"
    else:
        val = f"'{random_string()}'"

    sql = f"UPDATE {table} SET {set_col} = {val} WHERE {cols[0]} = {i}"
    return sql + ";"


def gen_delete(i):
    table = random.choice(TABLES)
    cols = COLUMNS[table]
    return f"DELETE FROM {table} WHERE {cols[0]} = {i};"


def gen_create_table(i):
    name = f"temp_table_{i}"
    cols_def = [
        f"id INT PRIMARY KEY",
        f"name VARCHAR(255) NOT NULL",
        f"value DECIMAL(10, 2) DEFAULT 0",
        f"active BOOLEAN DEFAULT 1",
        f"created_at TIMESTAMP NOT NULL",
    ]
    return f"CREATE TABLE {name} (\n    " + ",\n    ".join(cols_def) + "\n);"


def main():
    parser = argparse.ArgumentParser(description="Generate large SQL files for benchmarking")
    parser.add_argument("--lines", type=int, default=10000, help="Number of SQL statements")
    parser.add_argument("--output", type=str, default="large_test.sql", help="Output file path")
    parser.add_argument("--seed", type=int, default=42, help="Random seed for reproducibility")
    args = parser.parse_args()

    random.seed(args.seed)

    generators = [
        (gen_select, 0.50),
        (gen_insert, 0.20),
        (gen_update, 0.15),
        (gen_delete, 0.10),
        (gen_create_table, 0.05),
    ]

    with open(args.output, "w") as f:
        f.write(f"-- Auto-generated SQL benchmark file ({args.lines} statements)\n")
        f.write(f"-- Generated by generate_large_sql.py\n\n")

        for i in range(args.lines):
            r = random.random()
            cumulative = 0
            for gen_fn, weight in generators:
                cumulative += weight
                if r < cumulative:
                    f.write(gen_fn(i) + "\n")
                    break

    print(f"Generated {args.lines} SQL statements in {args.output}")
    import os
    size_kb = os.path.getsize(args.output) / 1024
    print(f"File size: {size_kb:.1f} KB")


if __name__ == "__main__":
    main()
